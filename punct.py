puncList = [".", ";", ":", "!", "?", "/", "\\", ",", "@", "$", "&", ")", "(", "\"", "#",
            "[", "]", "{", "}", "=", "+=", "-=", "*=", "/=", "//=", "%=", "&=", "|=",
            "^=", ">>=", "<<=", "**=", "+", '-', '==']


def is_punc(a):
    if a in puncList:
        return True
    else:
        return a in puncList
